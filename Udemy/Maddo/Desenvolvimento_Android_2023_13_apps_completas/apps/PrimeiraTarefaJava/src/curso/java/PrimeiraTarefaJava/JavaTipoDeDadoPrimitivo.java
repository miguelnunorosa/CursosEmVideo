package curso.java.PrimeiraTarefaJava;

public class JavaTipoDeDadoPrimitivo {

    public static void main(String[] args) {

        // boolean - true false 8 bits
        boolean ativo = true;

        // byte - -128 e 127 8 bits
        byte processo = 20;

        // short - -32.768 e 32.767 16 bits
        short valorQualquer = 30000;

        // int - 32 bits (2.147.483.647)
        int outroValor = 2000000000;

        // long - 64 bits
        long maisOutroValor = 1200l;

        // float
        float valorFloat = 1200.87f;

        // double
        double valorDouble = 50000;

        // char - '\u0000' = 0  '\uFFFF' 65.535
        char valorChar = '\uFFFF';

    }

}
